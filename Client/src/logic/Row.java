package logic;

public class Row {

	String s;
	int i;
	public Row(String s, int i) {
		super();
		this.s = s;
		this.i = i;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}	
}
